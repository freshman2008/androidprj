package com.exmaple.recyclerviewtest3;

public class MyItem {
    int goods_pic;
    String goods_flag;
    String goods_msg;
    String goods_price;
    String goods_old_price;

    public int getGoods_pic() {
        return goods_pic;
    }

    public void setGoods_pic(int goods_pic) {
        this.goods_pic = goods_pic;
    }

    public String getGoods_flag() {
        return goods_flag;
    }

    public void setGoods_flag(String goods_flag) {
        this.goods_flag = goods_flag;
    }

    public String getGoods_msg() {
        return goods_msg;
    }

    public void setGoods_msg(String goods_msg) {
        this.goods_msg = goods_msg;
    }

    public String getGoods_price() {
        return goods_price;
    }

    public void setGoods_price(String goods_price) {
        this.goods_price = goods_price;
    }

    public String getGoods_old_price() {
        return goods_old_price;
    }

    public void setGoods_old_price(String goods_old_price) {
        this.goods_old_price = goods_old_price;
    }
}
